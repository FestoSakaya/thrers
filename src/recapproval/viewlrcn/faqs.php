<h3 style="background:#FE4641; padding:8px; color:#ffffff;">Frequently Asked Questions</h3>



      <h4>What is subject to ethics review?</h4>
    
  
  
    
      <p style="font-size:14px;">All research with human subjects should obtain ethics  approval from a research ethics committee prior to its initiation. To meet the  definition of research with human subjects, the proposal must constitute  research and it must obtain information from human subjects. Research is  defined as any social science, biomedical, behavioral, or epidemiological  activity that entails systematic collection or analysis of data, with the  intent to develop generalizable knowledge. Human subjects are defined as human  beings (i) who become individually identifiable through investigators  collection, preparation, or use of biological material or medical or other  records, or (ii) who are exposed to manipulation, intervention, observation, or  other interaction with investigators either directly or through alteration of  their environment. Research with human subjects includes studies that use  tissues, samples, data or records from humans, as well as studies that involve  the conduction of interviews, focus groups or surveys, or the observation of  private behavior.</p>
      <p style="font-size:14px;">Activities that do not constitute research with human  subjects are exempted from ethics review. These are activities that do not aim  at the production of generalizable knowledge or do not involve human subjects  in the ways described above. For example, the following activities do not  constitute research with human subjects and are thus exempted from ethics  review:</p>
  <ul style="padding-left:20px; font-size:14px;">
        <li>Studies using data that are publicly available or  generated by observation of public behavior.</li>

        <li>Studies using data, samples, or tissue that already exist  (i.e. the data, samples or tissue have been obtained by others) and are  received by the research team in such a manner that the identity of the  individuals cannot be established.</li>

        <li>Public health activities conducted under recognized public  health authority with the intent to prevent or control of disease or injury,  including surveillance activities aimed only at the identification or control  of a health problem. These are activities that seek the benefit of the  participants or the participants communities, and do not intend to generate  knowledge that is applicable to other populations.</li>
  
        <li>Quality assurance activities or program evaluations aimed  at improving the program at stake, and with no intent to produce generalizable  knowledge.</li>
      </ul>
      <p style="font-size:14px;">Projects might involve multiple components, some of which  might constitute research with human subjects while other components might not.  The components that constitute research with human subjects should undergo  ethics review. Determinations about whether a proposal is exempted from ethics  review should be made by an independent party such as an ethics review  committee.</p>
    
  


  
    
