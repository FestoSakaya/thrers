      <h4>Which questions should guide the ethics review of research with human subjects?</h4>
    
  
  
    
      <ul style="padding-left:20px; font-size:14px;">
        <li>Social value: Can the study lead to improvements in health  or well-being? What is the potential value of the research for each of the  prospective beneficiaries?</li>
        <li>Scientific validity: Is the investigation methodologically  valid and scientifically (and statistically) sound? Will the study generate  valid and reliable data that can be generalizable? Is the study feasible? Does  the study design ensure participants the health-care interventions they are  entitled to? If not, are there methodologically compelling reasons and are  participants protected from serious harm?</li>

        <li>Fair participant selection: What are the criteria to include  and exclude participants? Is selection of participants based on scientific  criteria? Are research participants selected to minimize risks and maximize  potential benefits? If participants are vulnerable, are there any safeguards to  protect them? Are the risks and potential benefits of the study fairly  distributed?</li>
        <li>Favorable risk-benefit ratio: Can the risks for participants  be minimized? Can potential benefits for individuals and society be improved?  Do the potential benefits for society and individuals outweigh the risks?</li>
        <li>Informed consent: Is the information provided to potential  participants accurate, clear, relevant and complete? Are the recruitment  procedures, consent process and incentives appropriate for their culture and  context? Is there an appropriate plan for obtaining permission for those that  cant consent for themselves? Are the participants being made aware of their  right to refuse to participate and are they actually free to refuse?</li>
      </ul>
      <ul>
        <li>Respect for participants: How will the health and well-being  of participants be monitored to minimize harms? How will their privacy be  protected? Can participants withdraw from the study without penalty? What are  the plans of care after the study is completed? Will participants be given any  new information (including the results of the study)?</li>
      </ul>
